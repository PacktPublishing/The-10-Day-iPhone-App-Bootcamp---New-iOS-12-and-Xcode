// Loops

for number in 1...10 {
    print("I love loops! \(number)")
}

var favDrinks = ["Baja Blast", "Root Beer", "Pepsi"]

for index in 0...2 {
    print(favDrinks[index])
}

for drink in favDrinks {
    print(drink)
}

